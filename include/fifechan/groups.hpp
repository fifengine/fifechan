#ifndef INCLUDE_FIFECHAN_GROUPS_H_
#define INCLUDE_FIFECHAN_GROUPS_H_

/**
 * This file contains the Doxygen group definitions for this project.
 *
 * Groups are defined using the 'defgroup' tag.
 * They are referenced with the 'ingroup' tag.
 */

/**
 * @file groups.hpp
 * @brief Definition of logical groups for the fcn namespace.
 */

/**
 * @defgroup core Core & Architecture
 * @brief Central GUI management, focus handling, and base widget definitions.
 */

/**
 * @defgroup widgets Widgets
 * @brief Concrete UI elements like buttons, lists, and text fields.
 */

/**
 * @defgroup graphs Graph Widgets
 * @brief Specialized widgets for visualizing data (charts, graphs).
 */

/**
 * @defgroup events Events
 * @brief Classes representing user actions and system triggers.
 */

/**
 * @defgroup listeners Listeners
 * @brief Interfaces for observing events from widgets.
 */

/**
 * @defgroup graphics Graphics & Rendering
 * @brief Abstract drawing interfaces and backend implementations (OpenGL/SDL2).
 */

/**
 * @defgroup fonts Fonts
 * @brief Font rendering interfaces and implementations.
 */

/**
 * @defgroup input Input Handling
 * @brief Keyboard, mouse, and generic input polling.
 */

/**
 * @defgroup utils Utilities & Math
 * @brief Helper classes, math functions, and geometric primitives.
 */

#endif // INCLUDE_FIFECHAN_GROUPS_H_
